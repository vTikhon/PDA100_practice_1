# Задание
# Напишите программу для определения стоимости билетов в кино с учетом скидок.
# Скидки при покупке билетов за день до сеанса: скидка 5%.
# Вы можете купить билет со скидкой 10%.
# Для этого нужно собрать компанию не менее 5 человек. и, да, скидки суммируются :-)
# Исходные данные:
# Фильм "Паразиты", сеансы:
# • 12 часов – 250 руб,
# • 16 – 350 руб.
# Фильм "1917", сеансы:
# • 10 часов – 250 руб,
# • 13 – 350 руб.
# Порядок действий:
# • выбрать фильм выбрать дату (сегодня, завтра)
# • выбрать время,
# • указать количество билетов,
# • определить стоимость Пример выполнения программы:
# Программа для подсчета стоимости билетов в кино. Выбрать фильм: Паразиты
# Выбрать день (сегодня, завтра): завтра Выбрать время: 12 Выбрать количество билетов: 12
# Выбрали фильм: Паразиты День: завтра Время: 12 Количество билетов: 12 Результат: 2550.0 руб.
# ===================
# Программа для подсчета стоимости билетов в кино. Выбрать фильм: 8 Выбрать день (сегодня, завтра): 5
# Выбрать время: 5 Выбрать количество билетов: 4 Выбрали фильм: 8 День: 5 Время: 5 Количество билетов: 4 Ошибка ввода.

films = {
    "Паразиты": {12: 250, 16: 350},
    "1917": {10: 250, 13: 350}
}
days = ['сегодня', 'завтра']

def calculation(film, day, time, tickets):
    ticket_price = films[film][time]
    total_cost = ticket_price * tickets
    discount = 0
    if day == 'завтра':
        discount += 0.05
    if tickets >= 5:
        discount += 0.10
    total_cost *= (1 - discount)
    return total_cost

film = input(f'Выберите фильм ({", ".join(films.keys())}): ').strip()
if film not in films:
    print('Фильм не найден.')
else:
    day = input(f'Выберите день ({", ".join(days)}): ').strip().lower()
    if day not in days:
        print('День не распознан.')
    else:
        time = int(input(f'Выберите время ({", ".join(map(str, films[film]))}): '))
        if time not in films[film]:
            print('Время не найдено.')
        else:
            tickets = int(input('Выберите количество билетов: '))
            if tickets <= 0:
                print('Количество билетов должно быть положительным.')
            else:
                print(f' Выбрали фильм: {film}\n'
                      f' День: {day}\n'
                      f' Время: {time}\n'
                      f' Количество билетов: {tickets}\n'
                      f' Результат: {calculation(film, day, time, tickets):.2f} руб.')
